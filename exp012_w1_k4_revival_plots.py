def create_revival_table_4cycle():
    """
    Create DataFrame with all 30 revival parameters for 4-cycle from Dukes (2014) Table 4
    """
    # Data from Dukes (2014) Table 4
    data = {
        'Label': [f'R{i}' for i in range(1, 31)],
        'rho': [
            0.5, 0.5, 0.75, 0.5, 0.5, 0.75, 0.5, 0.5, 0.5, 0.5,
            0.8, 2/3, 0.5, 0.5, 0.75, 0.5, 0.5, 0.9, 2/3, 0.5,
            0.5, 3/4, 0.5, 0.5, 0.75, 0.5, 0.5, 0.8, 0.5, 0.5
        ],
        'delta_over_pi': [
            0.0, 1.0, 0.0, 0.5, 1.5, 1.0, 0.0, 1.0, 0.5, 1.5,
            0.0, 1.5, 0.0, 1.0, 0.0, 0.5, 1.5, 0.0, 0.5, 0.0,
            1.0, 0.5, 0.5, 1.5, 1.0, 0.0, 1.0, 1.0, 0.5, 1.5
        ],
        'N_revival': [
            6, 6, 6, 8, 8, 8, 10, 10, 12, 12,
            12, 12, 14, 14, 14, 16, 16, 16, 16, 18,
            18, 18, 20, 20, 20, 22, 22, 22, 24, 24
        ]
    }

    # Calculate delta
    data['delta'] = [d * np.pi for d in data['delta_over_pi']]

    # Add preliminary classification
    classifications = []
    for i in range(len(data['Label'])):
        rho = data['rho'][i]
        delta_pi = data['delta_over_pi'][i]

        if rho == 0.5:
            if delta_pi in [0.5, 1.5]:
                classifications.append('Class I (Perfect Alternating)')
            else:
                classifications.append('Class II (Fast Alternating)')
        elif rho == 2/3 or rho == 3/4:
            if delta_pi in [0.5, 1.5]:
                classifications.append('Class IIIa (High-Entropy Sustained)')
            else:
                classifications.append('Class IIIb (Complex Multi-Mode)')
        else:
            classifications.append('Class IIIc (Asymmetric Decaying)')

    data['Class'] = classifications

    df = pd.DataFrame(data)
    return df


