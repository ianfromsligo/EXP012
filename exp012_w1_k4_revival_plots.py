def create_coin_operator(rho, delta):
    """
    Create coin operator with standard convention (α = δ, β = 0)
    """
    alpha = delta
    beta = 0

    C = np.array([
        [np.sqrt(rho), np.sqrt(1 - rho) * np.exp(1j * alpha)],
        [np.sqrt(1 - rho) * np.exp(1j * beta), -np.sqrt(rho) * np.exp(1j * (alpha + beta))]
    ], dtype=complex)

    return C


def create_shift_operator(k=4):
    """
    Create shift operator for k-cycle
    """
    dim = 2 * k
    S = np.zeros((dim, dim), dtype=complex)

    # Coin |0⟩ moves forward
    for pos in range(k):
        next_pos = (pos + 1) % k
        S[next_pos, pos] = 1.0

    # Coin |1⟩ moves backward
    for pos in range(k):
        prev_pos = (pos - 1) % k
        S[k + prev_pos, k + pos] = 1.0

    return S


def initialize_state(rho, k=4):
    """
    Initialize state at position 0 with given coin superposition
    """
    dim = 2 * k
    psi = np.zeros(dim, dtype=complex)

    psi[0] = np.sqrt(rho)
    psi[k] = np.sqrt(1 - rho)

    return psi / np.linalg.norm(psi)


def von_neumann_entropy(rho_matrix):
    """
    Compute von Neumann entropy S = -Tr(ρ log₂ ρ)
    """
    eigenvalues = np.linalg.eigvalsh(rho_matrix)
    eigenvalues = np.maximum(eigenvalues, 0)

    entropy = 0
    for p in eigenvalues:
        if p > 1e-12:
            entropy -= p * np.log2(p)

    return entropy


def simulate_quantum_walk(rho, delta, steps=50, k=4):
    """
    Simulate quantum walk and return entropy at each step
    """
    # Create operators
    C = create_coin_operator(rho, delta)
    S = create_shift_operator(k)
    C_full = np.kron(C, np.eye(k))
    U = S @ C_full

    # Initialize state
    psi = initialize_state(rho, k)

    # Store entropy
    entropy = np.zeros(steps + 1)

    # Initial entropy
    psi_reshaped = psi.reshape(2, k)
    rho_coin = psi_reshaped @ psi_reshaped.conj().T
    entropy[0] = von_neumann_entropy(rho_coin)

    # Time evolution
    for t in range(1, steps + 1):
        psi = U @ psi
        psi_reshaped = psi.reshape(2, k)
        rho_coin = psi_reshaped @ psi_reshaped.conj().T
        entropy[t] = von_neumann_entropy(rho_coin)

    return entropy


