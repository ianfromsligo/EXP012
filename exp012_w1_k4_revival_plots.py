def analyze_entropy_pattern(S, N_rev, steps=50):
    """
    Analyze entropy pattern and extract metrics
    """
    # Basic metrics
    S_0 = S[0]
    S_rev = S[N_rev] if N_rev <= steps else np.nan
    S_max = np.max(S)
    S_mean_24 = np.mean(S[:24])
    S_mean_50 = np.mean(S)

    # Find peaks
    peaks, properties = signal.find_peaks(S, height=0.3, distance=2)
    peak_values = S[peaks]
    peak_positions = peaks

    # Find steps where S = 1.0 (within tolerance)
    max_entropy_steps = np.where(np.abs(S - 1.0) < 1e-10)[0]

    return {
        'S_0': S_0,
        'S_rev': S_rev,
        'S_max': S_max,
        'S_mean_24': S_mean_24,
        'S_mean_50': S_mean_50,
        'n_peaks': len(peaks),
        'peak_positions': peak_positions,
        'peak_values': peak_values,
        'max_entropy_steps': max_entropy_steps,
        'revival_diff': abs(S_rev - S_0) if not np.isnan(S_rev) else np.nan
    }


